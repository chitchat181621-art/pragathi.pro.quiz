let currentCategory = "";
let currentLevel = "";
let currentQuestionIndex = 0;
let score = 0;
let timer;
let timeLeft = 15;

function playSound(id) {
    const s = document.getElementById(id);
    s.currentTime = 0;
    s.play();
}

function showScreen(id) {
    document.querySelectorAll('.screen').forEach(s => s.classList.add('hidden'));
    const target = document.getElementById(id);
    target.classList.remove('hidden');
    playSound('sound-click');
}

function showCategories() { showScreen('category-screen'); }

function showLevels(category) {
    currentCategory = category;
    document.getElementById('selected-cat-title').innerText = category;
    showScreen('level-screen');
}

function startQuiz(level) {
    currentLevel = level;
    currentQuestionIndex = 0;
    score = 0;
    showScreen('quiz-screen');
    loadQuestion();
}

function loadQuestion() {
    const qData = questions[currentCategory][currentLevel][currentQuestionIndex];
    document.getElementById('question-text').innerText = qData.q;
    document.getElementById('question-number').innerText = `Question ${currentQuestionIndex + 1}`;
    
    const container = document.getElementById('options-container');
    container.innerHTML = '';
    document.getElementById('next-btn').classList.add('hidden');

    qData.a.forEach((opt, index) => {
        const btn = document.createElement('button');
        btn.innerText = opt;
        btn.className = "option-btn text-white font-semibold";
        btn.onclick = () => checkAnswer(index, btn);
        container.appendChild(btn);
    });

    startTimer();
}

function startTimer() {
    clearInterval(timer);
    timeLeft = 15;
    const display = document.getElementById('timer-ring');
    display.innerText = timeLeft;
    timer = setInterval(() => {
        timeLeft--;
        display.innerText = timeLeft;
        if (timeLeft <= 0) {
            clearInterval(timer);
            highlightCorrect();
            document.getElementById('next-btn').classList.remove('hidden');
        }
    }, 1000);
}

function checkAnswer(index, btn) {
    clearInterval(timer);
    const qData = questions[currentCategory][currentLevel][currentQuestionIndex];
    const allBtns = document.getElementById('options-container').children;

    if (index === qData.correct) {
        btn.classList.add('correct-reveal');
        score += 10;
        playSound('sound-correct');
    } else {
        btn.classList.add('wrong-reveal');
        allBtns[qData.correct].classList.add('correct-reveal');
        playSound('sound-wrong');
    }

    for (let b of allBtns) b.disabled = true;
    document.getElementById('next-btn').classList.remove('hidden');
}

function highlightCorrect() {
    const qData = questions[currentCategory][currentLevel][currentQuestionIndex];
    const allBtns = document.getElementById('options-container').children;
    allBtns[qData.correct].classList.add('correct-reveal');
    for (let b of allBtns) b.disabled = true;
}

function nextQuestion() {
    currentQuestionIndex++;
    const levelKeys =
    Object.keys(questions[currentCategory]);
    const currentLevelPosition = levelKeys.indexOf(currentLevel);

    if (currentQuestionIndex < questions[currentCategory][currentLevel].length) 
    {
        loadQuestion();
    }
     else if (currentLevelPosition !== -1 && levelKeys[currentLevelPosition + 1]) {
        currentLevel = levelKeys[currentLevelPosition + 1];
            currentQuestionIndex = 0; // Reset question count for the new level
             

            showLevelModal(currentLevel);
            loadQuestion();
        }
        else{
            showScorePage();
        }
    }
function showLevelModal(nextLevelName) {
    const modal = document.getElementById('level-modal');
    document.getElementById('level-msg').innerText = "Ready for " + nextLevelName + "?";
    
    modal.classList.remove('hidden');

    // When button is clicked, hide modal and load the next level questions
    document.getElementById('modal-btn').onclick = function() {
        modal.classList.add('hidden');
        loadQuestion(); 
    };
}
function handleLogin() {
    const nameInput = document.getElementById('username');
    const name = nameInput.value.trim();

    if (name === "") {
        alert("Please enter your name!");
        return;
    }

    const formData = new FormData();
    formData.append('full_name', name);

    // This sends the name to the database
    fetch('login.php', {
        method: 'POST',
        body: formData
    })
    .then(() => {
        // THIS PART "TAKES" YOU TO THE QUESTIONS
        document.getElementById('login-screen').classList.add('hidden');
        document.getElementById('category-screen').classList.remove('hidden');
        
        // If your questions start with a specific function, call it here:
        // startQuiz(); 
    })
    .catch(error => console.error("Error:", error));
}

